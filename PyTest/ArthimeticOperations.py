
def add2numbers(num1, num2):
    return num1+num2

def subtract2numbers(num1, num2):
    return num1-num2

def multiply2numbers(num1, num2):
    return num1*num2

def divide2numbers(num1, num2):
    return num1/num2

def truncationdivision(num1, num2):
    return num1//num2

def getremainderquotient(divideby, divisor):
    return divmod(divideby, divisor)